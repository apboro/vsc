<p style="text-align: center"><b>1. Предмет договора</b></p>
<p>1.1. Предметом настоящего Договора является организация Исполнителем участия Потребителя в спортивных мероприятиях
    среди коллективов физической культуры и спортивных клубов с обеспечением
    его проживания в период с <b>{{ $service_start_date }}</b> по <b>{{ $service_end_date }}</b> на территории <b>{{ $training_base_name }}</b>, расположенного по адресу: <b>{{ $training_base_address }}</b>
</p>

<br/>
<p style="text-align: center"><b>2. Обязательства сторон</b></p>
<p><b>2.1. Исполнитель обязуется: </b></p>
<p>2.1.1. Заключить договор на оказание возмездных услуг с ИП Рапопорт Александр Александрович на предоставление комплекса услуг по организации проживания Потребителя в период проведения спортивных мероприятий среди коллективов физической культуры и спортивных клубов в период с <b>{{ $service_start_date }}</b> по <b>{{ $service_end_date }}</b></p>
<p>2.1.2. Организовать доставку Потребителя до территории <b>{{ $training_base_name }}</b></p>
<p>2.1.3. Обеспечить охрану здоровья и безопасность пребывания Потребителя в <b>{{ $training_base_name }}</b>,  нести ответственность за ежедневное его безопасное пребывание в период с <b>{{ $service_start_date }}</b> по <b>{{ $service_end_date }}</b> Организовать четырехразовое питание Потребителя в период его нахождения в <b>{{ $training_base_name }}</b>.</p>
<p>2.1.4. Организовать участие Потребителя в спортивных мероприятиях среди коллективов физической культуры и спортивных клубов;</p>
<p>2.1.5. В случае необходимости оказать Потребителю первую медицинскую помощь и обеспечить его доставку в лечебное учреждение (по согласованию с Заказчиком);</p>
<p>2.1.6. Уведомлять Исполнителя о ненадлежащем поведении Исполнителя, нарушении им этических норм и норм общественного порядка;</p>

<br/>
<p><b>2.2. Заказчик обязуется</b></p>
<p>2.2.1. Обеспечить Потребителя необходимыми личными вещами и предметами гигиены для пребывания в <b>{{ $training_base_name }}</b>.</p>
<p>2.2.2. Довести до сведения Заказчика факт наличия у Потребителя существенных медицинских противопоказаний или хронических заболеваний, которые могут негативно отразиться на здоровье Потребителя во время отдыха.</p>
<p>2.2.3. За неделю до выезда предоставить Заказчику:</p>
<p>а) справку об отсутствии инфекционных заболеваний по месту жительства Ребёнка;</p>
<p>б) медицинскую справку из школы для ребёнка, отъезжающего в лагерь;</p>
<p>в) результаты анализов я/г и энтеробиоз;</p>
<p>г) копии медицинского полиса, сертификата прививок;</p>
<p>д) согласие на пребывание ребенка в условиях пандемии COVID-19;</p>
<p>2.2.4. Проинформировать Потребителя о требованиях, предъявляемых к нему в период пребывания в <b>{{ $training_base_name }}</b> и последствиях их нарушения</p>
<p>2.2.5. Внести авансовый платеж в размере <b>{{$advance_payment}}</b> рублей не позднее <b>{{$date_advance_payment}}</b> и оплатить полностью стоимость настоящего Договора не позднее <b>{{$date_deposit_funds}}</b>.</p>
<p>2.2.6. В случае нанесения ущерба имуществу Исполнителя или ИП Рапопорта Александра Александровича в результате нарушения Потребителем установленных настоящим договором требований возместить стоимость нанесенного им ущерба.</p>
<p>2.2.7. На основании уведомления Заказчика забрать Потребителя с территории <b>{{ $training_base_name }}</b> в случаях:</p>
<p>- грубого нарушения им мер собственной безопасности и правил внутреннего распорядка, включая самовольный уход с территории <b>{{ $training_base_name }}</b>;</p>
<p>- грубого нарушения распорядка дня, дисциплины, норм поведения в общественных местах;</p>
<p>- совершения любого рода противоправных действий;</p>
<p>- нанесения морального или физического ущерба другим детям;</p>
<p>- нанесения значительного материального ущерба имуществу Исполнителя или ИП Рапопорта Александра Александровича;</p>
<p>- выявления у Потребителя скрытых Заказчиком при заключении настоящего Договора и предоставлении медицинских справок существенных медицинских противопоказаний или хронических заболеваний, которые могут негативно отразиться на здоровье Потребителя во время отдыха.</p>

<br/>
<p><b>2.3. Потребитель обязуется</b></p>
<p>2.3.1. Принимать участие в спортивных мероприятиях среди коллективов физической культуры и спортивных клубов;</p>
<p>2.3.2. Выполнять распорядок дня, установленный на каждый день;</p>
<p>2.3.3. Соблюдать меры собственной безопасности, пожарной безопасности; правила участия в спортивных мероприятиях среди коллективов физической культуры и спортивных клубов;</p>
<p>2.3.4. Постоянно находиться в составе своей группы;</p>
<p>2.3.5. Принимать участие в самообслуживающем труде, содержать в порядке личные вещи;</p>
<p>2.3.6. Выполнять санитарно-гигиенические требования; следить за своим внешним видом, одеждой;</p>
<p>2.3.7. Бережно относиться к имуществу Исполнителя или ИП Рапопорта Александра Александровича;</p>
<p>2.3.8. В случае недомогания или получения травмы немедленно известить Исполнителя или сотрудника ИП Рапопорта Александра Александровича;</p>
<p>2.3.9. Не нарушать общественного порядка, вести себя социально ответственно, с уважением и тактом относиться к иным спортсменам;</p>
<p>2.3.10. С уважением относиться к сотрудникам Заказчика, ИП Рапопорта Александра Александровича, выполнять их указания.</p>
<p>2.3.11. Не совершать действий, наносящих вред своему здоровью и здоровью окружающих.</p>
<p>2.3.12. Бережно относиться к природе.</p>

