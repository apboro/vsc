<p>3.1.2. Досрочно прекратить исполнение услуг по настоящему договору и известить Заказчика о необходимости забрать Потребителя с территории <b>{{ $training_base_name }}</b> по состоянию здоровья, препятствующего его дальнейшему пребыванию в Лагере.</p>
